PHASE = "OPTION_C_C1A_BOUNDED_CANARY"
AUTHORIZED_SCOPE = "OPTION_C_C1A_CANARY_RECONCILIATION_ONLY"

REQUIRED_EVENT_FIELDS = (
    "condition_id",
    "tx_hash",
    "block_time",
    "makerAssetId",
    "takerAssetId",
    "makerAmountFilled",
    "takerAmountFilled",
    "source_provenance",
)

_SCI_RE = re.compile(r"[eE]")
_INT_RE = re.compile(r"^[0-9]+$")

# tx_hash_relation values for row-level artifacts (patch requirement 4).
REL_DUNE_ONLY = "DUNE_ONLY"
REL_LOCAL_ONLY = "LOCAL_ONLY"
REL_OVERLAP = "OVERLAP"

# ---------------------------------------------------------------------------
# Stop / outcome codes (C1R addendum section 9, extended by this patch)
# ---------------------------------------------------------------------------
C1_SOURCE_SCHEMA_UNVERIFIED = "C1_SOURCE_SCHEMA_UNVERIFIED"
C1_ORDERING_KEY_MISSING = "C1_ORDERING_KEY_MISSING"
C1_ROW_EXPLOSION = "C1_ROW_EXPLOSION"
C1_LOCAL_TX_HASH_REPRODUCTION_ONLY = "C1_LOCAL_TX_HASH_REPRODUCTION_ONLY"
C1_INDEXER_SHAPED = "C1_INDEXER_SHAPED"
C1_SIDE_IDENTITY_UNRESOLVED = "C1_SIDE_IDENTITY_UNRESOLVED"

# New in this patch: an empty Dune export is a distinct, non-clear outcome -
# it must never fall through to a "clean" result. It is raised as a CanaryHalt
# like every other non-success path, not silently treated as vacuously OK.
C1_SOURCE_EMPTY = "C1_SOURCE_EMPTY"

# New in this patch: the automatic outcome of a non-halting run. This code
# means "ran to completion without tripping a hard stop" - nothing more. It is
# NOT a design-clear verdict.
C1_CANARY_EXECUTED_NEEDS_REVIEW = "C1_CANARY_EXECUTED_NEEDS_REVIEW"

# Retained as a defined label only. This module never assigns this value to
# any run automatically - it exists so the Orchestrator has a fixed, agreed
