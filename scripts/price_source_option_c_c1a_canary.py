"""Option C, C1A - bounded canary reconciliation against decoded OrderFilled rows.

CODE / TEST ONLY. No network call. No Dune call. No API/RPC call performed by
this module or by Claude. This module CONSUMES a Dune CSV export that the user
fetches manually, per the documented flow in project_context/DUNE_DATA_NOTES.md
section 7, using the query text generated by
scripts/price_source_option_c_c1a_manifest.py.

Governing specs:
- project_context/SPEC_price_source_option_c_onchain.md (Revision 3)
- project_context/SPEC_price_source_option_c_onchain_C1R_addendum.md (Patch 1)

What this file IS:
  - A reconciliation pass: given the fixed C1A selector manifest (output of the
    manifest builder) and a Dune CSV export of decoded OrderFilled rows, it
    validates schema/precision, enforces hard per-condition and global row caps
    with fail-fast, tags each row's matched side (side_0/side_1) by raw token
    match ONLY (no interpretation), and compares the retrieved tx_hash set
    against LOCAL trades for the same fixed conditions/windows/token pairs to
    produce a coverage diagnostic - not a price artifact.

What this file is NOT (hard non-goals, enforced):
  - No canonical_side_price, no target, no score, no probe feature.
  - No OrdersMatched. No wallet aggregation beyond raw per-row asset/amount
    fields already on the event row.
  - Coverage diagnostics only. A non-halt run reports `C1_CANARY_EXECUTED_NEEDS_REVIEW`,
    never `C1_CANARY_DESIGN_CLEAR` - this module never self-certifies design-clear
    status. `C1_CANARY_DESIGN_CLEAR` is a label the Orchestrator applies, if
    warranted, only after reviewing the persisted artifacts by hand.
"""

